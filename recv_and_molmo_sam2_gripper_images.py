def extract_points(text, width, height):
    """
    Parses XML-like <point> format generated by Molmo to extract image coordinates
    Returns a list of tuples: [(x1, y1), (x2, y2), ...]
    """
    points = []
    
    # 1. Molmo 2 format: <points coords="1 1 418 735 2 546 735">
    for match in re.finditer(r'<points?[^>]*coords="([0-9\s]+)"', text):
        coords_str = match.group(1)
        coords = [int(v) for v in coords_str.split()]
        
        if len(coords) % 3 == 1 and len(coords) >= 4:
            coords = coords[1:] 
            
        if len(coords) % 3 == 0:
            for i in range(0, len(coords), 3):
                norm_x = coords[i+1] / 1000.0
                norm_y = coords[i+2] / 1000.0
                pixel_x = int(norm_x * width)
                pixel_y = int(norm_y * height)
                points.append((pixel_x, pixel_y))
                
        elif len(coords) > 0 and len(coords) % 2 == 0:
            for i in range(0, len(coords), 2):
                norm_x = coords[i] / 1000.0
                norm_y = coords[i+1] / 1000.0
                pixel_x = int(norm_x * width)
                pixel_y = int(norm_y * height)
                points.append((pixel_x, pixel_y))

    # 2. Older fallback Molmo format: <point x="0.123" y="0.456">
    pattern = r'<point.*?x="([0-9.]+)".*?y="([0-9.]+)".*?>'
    for match in re.finditer(pattern, text):
        norm_x = float(match.group(1))
        norm_y = float(match.group(2))
        
        if norm_x > 1.0 or norm_y > 1.0:
            norm_x = norm_x / 100.0
            norm_y = norm_y / 100.0
            
        pixel_x = int(norm_x * width)
        pixel_y = int(norm_y * height)
        points.append((pixel_x, pixel_y))
        
    return points

